num1 = 1
num2 = 3
total = num1 + num2
print(f"1 + 3 = {total}")


# Default is floating result
print(f"4/5 = {4/5}")

# // floor division
print(f"4//5 = {4//5}")

# % returns the remainder of the division
print(f"4%5 = {4%5}")

# ** Power operator
print(f" 2 power 3 = {2**3}")

# = assignment operator
pi = 3.14
print(f"PI = {pi}")


